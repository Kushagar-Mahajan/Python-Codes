#basics about booleans and comparison operators

a = 2 < 3 #check whether this is true of false
print(a) #prints true or false of boolean a

b = type(a) #check the class of a
print(b)

c = 2 == 3 #check whether it is true or false
print(c)

d = 2 != 3 #here != is not equal to
print(d)

e = 4 >= 3 #here >= is checked
print(e)