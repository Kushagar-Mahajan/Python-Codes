#rounding off to nearest number

import math #import python maths module

a = round(2.2) #round of the number
print("Round off of 2.2 is",a)

b = math.floor(3.8) #decrease to lower value of round off
print("Floor of 3.8 is",b)

c = math.ceil(2.1) #increase ro upper value of round off
print("Ceil of 2.1 is",c)

d = math.pi #prints value of pi
print("Value of pi is",d)

e = math.e #prints value of exponent
print("Value of exponent is",e)

f = math.floor(math.sin(math.pi)) #used in radians in  not in fraction
print("Value of sine is",f)

g = math.asin(0) #used to print inverse of sine function
print("Value of inverse of sine is",g)

h = math.hypot(3,4) #used to find hypotnuse in right angle triangle
print("Hypotnuse of rt. angled triangle of side 3 & 4 is", h)

i = math.pow(9,2) #used to find square
print("Answer of 9^2 is",i)

j = math.exp(2)
print("Answer of exp(2) is",j)

k = math.log(23)
print("Answer of log(23) is",k)
